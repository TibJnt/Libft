#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int	i;

	if ((char)c == '\0')
		return ((char *)&str[ft_strlen((char *)str)]);
	i = 0;
	while (str[i])
	{
		if (str[i] == (char)c)
			return ((char *)&str[i]);
		i++;
	}
	return (0);
}
