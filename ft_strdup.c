#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tab;
	int		s_size;
	int		i;

	s_size = ft_strlen((char *)s1);
	tab = (char *)malloc((s_size + 1) * sizeof(char));
	if (!tab)
		return (0);
	i = 0;
	while (s1[i])
	{
		tab[i] = s1[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
